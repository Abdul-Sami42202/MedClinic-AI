// ========== Helpers ==========
export function $(selector) {
  return document.querySelector(selector);
}

export function $$(selector) {
  return document.querySelectorAll(selector);
}

export function getStatusBadge(status) {
  const map = {
    pending: 'badge-warning',
    confirmed: 'badge-info',
    completed: 'badge-success',
    cancelled: 'badge-danger',
  };
  return `<span class="badge ${map[status] || 'badge-gray'}">${status}</span>`;
}

export function getRoleBadge(role) {
  const map = {
    admin: 'badge-danger',
    doctor: 'badge-primary',
    receptionist: 'badge-info',
    patient: 'badge-success',
  };
  return `<span class="badge ${map[role] || 'badge-gray'}">${role}</span>`;
}

export function getPlanBadge(plan) {
  return plan === 'pro'
    ? '<span class="badge badge-primary">PRO</span>'
    : '<span class="badge badge-gray">FREE</span>';
}

export function debounce(fn, delay = 300) {
  let timer;
  return (...args) => {
    clearTimeout(timer);
    timer = setTimeout(() => fn(...args), delay);
  };
}
