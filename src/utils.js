// ========== Toast Notifications ==========
let toastContainer = null;

function ensureToastContainer() {
  if (!toastContainer) {
    toastContainer = document.createElement('div');
    toastContainer.className = 'toast-container';
    document.body.appendChild(toastContainer);
  }
  return toastContainer;
}

export function showToast(message, type = 'info') {
  const container = ensureToastContainer();
  const toast = document.createElement('div');
  toast.className = `toast ${type}`;
  toast.innerHTML = `
    <span class="toast-msg">${message}</span>
    <button class="toast-close" onclick="this.parentElement.remove()">&times;</button>
  `;
  container.appendChild(toast);
  setTimeout(() => {
    toast.style.animation = 'slideOut 0.3s ease forwards';
    setTimeout(() => toast.remove(), 300);
  }, 4000);
}

// ========== Modal ==========
let modalOverlay = null;

function ensureModalOverlay() {
  if (!modalOverlay) {
    modalOverlay = document.createElement('div');
    modalOverlay.className = 'modal-overlay';
    modalOverlay.addEventListener('click', (e) => {
      if (e.target === modalOverlay) closeModal();
    });
    document.body.appendChild(modalOverlay);
  }
  return modalOverlay;
}

export function openModal(title, bodyHTML, footerHTML = '', large = false) {
  const overlay = ensureModalOverlay();
  overlay.innerHTML = `
    <div class="modal ${large ? 'modal-lg' : ''}">
      <div class="modal-header">
        <h3>${title}</h3>
        <button class="modal-close" id="modal-close-btn">&times;</button>
      </div>
      <div class="modal-body">${bodyHTML}</div>
      ${footerHTML ? `<div class="modal-footer">${footerHTML}</div>` : ''}
    </div>
  `;
  overlay.classList.add('active');
  overlay.querySelector('#modal-close-btn').addEventListener('click', closeModal);
  return overlay;
}

export function closeModal() {
  if (modalOverlay) {
    modalOverlay.classList.remove('active');
  }
}

// ========== Loading Overlay ==========
let loadingOverlay = null;

function ensureLoadingOverlay() {
  if (!loadingOverlay) {
    loadingOverlay = document.createElement('div');
    loadingOverlay.className = 'loading-overlay';
    loadingOverlay.innerHTML = '<div class="spinner"></div>';
    document.body.appendChild(loadingOverlay);
  }
  return loadingOverlay;
}

export function showLoading() {
  ensureLoadingOverlay().classList.add('active');
}

export function hideLoading() {
  if (loadingOverlay) loadingOverlay.classList.remove('active');
}

// ========== Form Validation ==========
export function validateForm(fields) {
  let valid = true;
  // Clear previous errors
  document.querySelectorAll('.form-error').forEach(el => el.remove());
  document.querySelectorAll('.form-control.error').forEach(el => el.classList.remove('error'));

  for (const field of fields) {
    const input = document.getElementById(field.id);
    if (!input) continue;
    let errorMsg = '';

    const value = input.value.trim();
    if (field.required && !value) {
      errorMsg = `${field.label || 'This field'} is required`;
    } else if (field.type === 'email' && value && !/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(value)) {
      errorMsg = 'Please enter a valid email';
    } else if (field.minLength && value.length < field.minLength) {
      errorMsg = `Must be at least ${field.minLength} characters`;
    } else if (field.type === 'phone' && value && !/^[\d\s\-+()]{7,15}$/.test(value)) {
      errorMsg = 'Please enter a valid phone number';
    }

    if (errorMsg) {
      valid = false;
      input.classList.add('error');
      const errEl = document.createElement('div');
      errEl.className = 'form-error';
      errEl.textContent = errorMsg;
      input.parentElement.appendChild(errEl);
    }
  }
  return valid;
}

// ========== Date Formatting ==========
export function formatDate(date) {
  if (!date) return '';
  const d = date instanceof Date ? date : date.toDate ? date.toDate() : new Date(date);
  return d.toLocaleDateString('en-US', { year: 'numeric', month: 'short', day: 'numeric' });
}

export function formatDateTime(date) {
  if (!date) return '';
  const d = date instanceof Date ? date : date.toDate ? date.toDate() : new Date(date);
  return d.toLocaleDateString('en-US', {
    year: 'numeric', month: 'short', day: 'numeric',
    hour: '2-digit', minute: '2-digit'
  });
}

export function formatTime(timeStr) {
  if (!timeStr) return '';
  const [h, m] = timeStr.split(':');
  const hour = parseInt(h);
  const ampm = hour >= 12 ? 'PM' : 'AM';
  const h12 = hour % 12 || 12;
  return `${h12}:${m} ${ampm}`;
}

export function getTodayStr() {
  return new Date().toISOString().split('T')[0];
}

